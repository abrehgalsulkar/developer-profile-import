package com.devprofiles.developerprofileimport.config;

import java.nio.file.Path;
import java.nio.file.Paths;
import org.springframework.context.annotation.Configuration;
import org.springframework.web.servlet.config.annotation.ResourceHandlerRegistry;
import org.springframework.web.servlet.config.annotation.WebMvcConfigurer;

@Configuration
public class WebStaticResourceConfig implements WebMvcConfigurer {

  @Override
  public void addResourceHandlers(ResourceHandlerRegistry registry) {
    Path imageDirectory = Paths.get("storage", "developers").toAbsolutePath();
    String location = imageDirectory.toUri().toString();

    registry.addResourceHandler("/developer-images/**")
        .addResourceLocations(location);
  }
}
